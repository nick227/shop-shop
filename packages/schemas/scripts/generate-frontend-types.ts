/**
 * Extract TypeScript type from Zod schema
 */
function zodToTypeString(schema: z.ZodTypeAny, typeName: string): string {
  // For now, use z.infer approach
  // This generates from the actual Zod schemas
  return `export type ${typeName} = z.infer<typeof ${typeName}Schema>`
}

/**
 * Generate type exports from DTO module
 */
function generateTypesFromDTO(dtoModule: any, moduleName: string): string[] {
  const types: string[] = []
  
  for (const [exportName, exportValue] of Object.entries(dtoModule)) {
    if (exportName.endsWith('Schema') && exportValue instanceof z.ZodType) {
      const typeName = exportName.replace('Schema', '')
      
      // Skip internal schemas
      if (typeName.includes('Internal') || typeName.includes('Private')) {
        continue
      }
      
      types.push(typeName)
    }
  }
  
  return types
}

